#include <stdio.h>
#include <math.h>

int main() {
    int x, y, z; // оголошення змінних для введення користувачем
    double numerator, denominator, a; // оголошення змінних для обчислення чисельника, знаменника та значення функції a

    while (1) {
        printf("Введіть цілі значення x, y, z: ");
        if (scanf("%d %d %d", &x, &y, &z) != 3) {
            printf("Невірне введення. Спробуйте ще раз.\n");
            while (getchar() != '\n'); // очистити буфер
            continue;
        }

        // Перевірка: корені не з від'ємного числа
        if (y * z < 0 || x + 2 * sqrt(y * z) < 0 || y < 0 || x * z < 0) { // якщо y * z < 0 або підкореневий вираз від'ємний
            printf("Неможливо обчислити: підкореневий вираз від'ємний. Спробуйте інші значення.\n");
            continue;
        }

        numerator = sqrt(x + 2 * sqrt(y * z)); // обчислення чисельника
        denominator = sqrt(y) - 2 * sqrt(x * z); // обчислення знаменника

        // Перевірка ділення на нуль
        if (denominator == 0) { // якщо знаменник дорівнює нулю
            printf("Неможливо обчислити: ділення на 0. Спробуйте інші значення.\n");
            continue; // продовжити цикл для нового введення
        }

        a = numerator / denominator; // обчислення значення функції a

        printf("Значення функції a: %.4lf\n", a);
        break; // Вихід з циклу, якщо все добре
    }

    return 0;
}   
