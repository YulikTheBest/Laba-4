#include <stdio.h>

int main() {
    int x, i = 10, sum = 0;
    printf("Введіть ціле число x: ");
    scanf("%d", &x);

    do { // цикл для перебору чисел від 10 до 100
        if (i % 2 == 0 && i > x) // перевірка на парність і більше за x
            sum += i;// додавання до суми
        i++;// збільшення лічильника
    } while (i <= 100);//   умова продовження циклу

    printf("Сума парних чисел більше за %d у діапазоні [10;100]: %d\n", x, sum);
    return 0;
}
